using System;

namespace CalculatorClass
{
    public class CalcClass
    {
        public double AdditionMethod(double operand1, double operand2) => (operand1 + operand2);
        public double SubtractionMethod(double operand1, double operand2) => (operand1 - operand2);
        public double MultiplicationMethod(double operand1, double operand2) => (operand1 * operand2);
        public double DivisionMethod(double operand1, double operand2) => (operand1 / operand2);
    }
}